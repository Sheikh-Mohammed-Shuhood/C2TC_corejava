package Day3;

public class Person {
	private String name;
	private String gender;
	private int  age;
	private int income;
	private int tax;
	
	public String getName() {
		return name;
	}
	public void setName(String pname) {
		this.name = pname;
	}
	
	
	public String getGender() {
		return gender;
	}
	public void setGender(String pgender) {
		this.gender = pgender;
	}
	
	
	public int getAge() {
		return age;
	}
	public void setAge(int page) {
		this.age = page;
	}
	
	
	public int getIncome() {
		return income;
	}
	public void setIncome(int pincome) {
		this.income = pincome;
	}
	
	
	public int getTax() {
		return tax;
	}
	public void setTax(int ptax) {
		this.tax = ptax;
	}

}
